#include <stdio.h>


//----------------------------------------------------------------------------------------//


int primo(int n);
int prox_primo(int n);


//----------------------------------------------------------------------------------------//


int main(void)
{
    int n, i = 0;
    scanf("%d", &n);

    printf("%d = ", n);

    while(n != 1)
    {
        int aux;
        int ft = prox_primo(i);
        if (n % ft == 0)
        {
            if (n == ft)
            {
                aux = n;
            }

            n = n / ft;
            
            if (aux == ft)
            {
                printf("%d\n", ft);
            }

            else
            {
                printf("%d . ", ft); 
            }
        }

        else
        {
            i++;
        }
    }


    return 0;
}


//----------------------------------------------------------------------------------------//


int primo(int n)
{
    if (n < 2)
    {
        return 0;
    }

    if (n = 2)
    {
        return 1;
    }

    int p = 1;

    for (int i = 2; i < n; i++)
    {
        if (n % i == 0)
        {
            p = 0;
            break;
        }
    }

    return p;
}


//----------------------------------------------------------------------------------------//


int prox_primo(int n)
{
    int i = n + 1;
    while(!primo(i))
    {
        i++;
    }

    return i;
}


